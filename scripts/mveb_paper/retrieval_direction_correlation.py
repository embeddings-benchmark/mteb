"""Pairwise Spearman correlation across MVEB retrieval directions.

Loads results through ``mteb.ResultCache`` (defaults to ``~/.cache/mteb`` and
pulls the remote ``embeddings-benchmark/results`` repo), groups retrieval tasks
by direction (T2V, V2T, A2V, V2A, AT2V, VA2T, VT2A, T2VA), computes the
per-direction mean nDCG@10 for each audio-capable model, and emits the
upper-triangle pairwise Spearman correlation as a LaTeX table.
"""

from __future__ import annotations

import argparse
import re
import sys
from collections import defaultdict
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent.parent))

import mteb  # noqa: E402
import numpy as np  # noqa: E402
import pandas as pd  # noqa: E402

OUT_DIR = Path(__file__).parent

DIR_ORDER = ["T2V", "V2T", "A2V", "V2A", "AT2V", "VA2T", "VT2A", "T2VA"]
DIR_LABEL = {
    "T2V": r"$T\!\to\!V$",
    "V2T": r"$V\!\to\!T$",
    "A2V": r"$A\!\to\!V$",
    "V2A": r"$V\!\to\!A$",
    "AT2V": r"$AT\!\to\!V$",
    "VA2T": r"$VA\!\to\!T$",
    "VT2A": r"$VT\!\to\!A$",
    "T2VA": r"$T\!\to\!VA$",
}

DIRECTION_RX = re.compile(
    r"(?P<dataset>.+?)(?P<dir>T2V|V2T|A2V|V2A|AT2V|VA2T|VT2A|T2VA)Retrieval(?:\.v\d+)?$"
)

# For rank correlation we keep ALL audio-capable models, including
# jina-omni (excluded from the absolute-score audio-delta analysis):
# even models with weak absolute performance contribute valid rank
# information across directions.
EXCLUDE_MODELS = set()


def main(cache: mteb.ResultCache, out_dir: Path):
    # Retrieval tasks in the video/AV roster, loaded via ResultCache.
    tasks = [
        t
        for t in mteb.get_tasks(modalities=["video"], exclude_beta=False)
        if DIRECTION_RX.match(t.metadata.name)
    ]
    scores = (
        cache.load_results(tasks=tasks, require_model_meta=False)
        .to_dataframe()
        .set_index("task_name")
    )

    rows = []
    for model_name in [c for c in scores.columns]:
        if model_name in EXCLUDE_MODELS:
            continue
        per_direction = defaultdict(list)
        for task_name, val in scores[model_name].items():
            if pd.isna(val):
                continue
            m = DIRECTION_RX.match(task_name)
            if not m:
                continue
            per_direction[m.group("dir")].append(float(val))
        # need ≥6 directions to be useful for correlation
        if len(per_direction) < 6:
            continue
        row = {"model": model_name}
        for d in DIR_ORDER:
            if d in per_direction:
                row[d] = float(np.mean(per_direction[d]))
        rows.append(row)

    df = pd.DataFrame(rows).set_index("model")
    print(f"Models with ≥6 retrieval directions: {len(df)}")
    print(df.notna().sum())
    print()

    # Drop directions with too few models (need ≥6 to be meaningful)
    keep = [d for d in DIR_ORDER if df[d].notna().sum() >= 6]
    df = df[keep]
    print(f"Directions kept: {keep}\n")

    # Pairwise Spearman over models that have BOTH directions
    n = len(keep)
    corr_arr = np.full((n, n), np.nan)
    for i, d1 in enumerate(keep):
        a = df[d1].dropna()
        for j in range(i, n):
            d2 = keep[j]
            b = df[d2].dropna()
            common = a.index.intersection(b.index)
            if len(common) >= 4:
                rho = a.loc[common].rank().corr(b.loc[common].rank())
                if not np.isnan(rho):
                    corr_arr[i, j] = float(rho)
                    corr_arr[j, i] = float(rho)
    corr = pd.DataFrame(corr_arr, index=keep, columns=keep)
    print("Spearman correlation:")
    print(corr.round(2).to_string())

    # Emit LaTeX upper triangle
    out_dir.mkdir(parents=True, exist_ok=True)
    out = out_dir / "retrieval_direction_correlation.tex"
    lines = [
        r"% AUTOGENERATED by scripts/mveb_paper/retrieval_direction_correlation.py — do not edit by hand.",
        r"\begin{table*}[t]",
        r"\centering",
        r"\small",
        r"\caption{Pairwise Spearman correlation $\rho$ across the eight retrieval directions, computed across the audio-capable models in our roster. Each cell reports $\rho$ over the models that have results in both directions. Higher $\rho$ means models that rank well on one direction also rank well on the other (the two directions measure overlapping capabilities). Bold cells: $|\rho| > 0.85$.}",
        r"\label{tab:retrieval-pairwise}",
        r"\begin{tabular}{l" + "c" * n + "}",
        r"\toprule",
        " & ".join([""] + [DIR_LABEL[d] for d in keep]) + r" \\",
        r"\midrule",
    ]
    for i, d1 in enumerate(keep):
        cells = [DIR_LABEL[d1]]
        for j, d2 in enumerate(keep):
            if j < i:
                cells.append("")
            else:
                v = corr.loc[d1, d2]
                if pd.isna(v):
                    cells.append("--")
                else:
                    s = f"${v:+.2f}$".replace("+", "\\,") if v >= 0 else f"${v:+.2f}$"
                    if abs(v) > 0.85 and j > i:
                        s = r"\textbf{" + s + r"}"
                    cells.append(s)
        lines.append(" & ".join(cells) + r" \\")
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table*}", ""])
    out.write_text("\n".join(lines))
    print(f"\nWrote {out}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--cache-path",
        default=None,
        help="Path to a results cache directory (default: ~/.cache/mteb).",
    )
    parser.add_argument(
        "--download",
        action="store_true",
        help="Force-refresh the remote results repo before loading "
        "(otherwise the local cache is used, downloading only if absent).",
    )
    parser.add_argument(
        "--out-dir",
        default=str(OUT_DIR),
        help="Directory the .tex table is written to (default: this script's "
        "folder). Point at the paper's tables/ directory to write there directly.",
    )
    args = parser.parse_args()
    cache = mteb.ResultCache(cache_path=args.cache_path)
    if args.download or not cache.has_remote:
        cache.download_from_remote()
    main(cache=cache, out_dir=Path(args.out_dir))
